package FundamentosDaLinguagem;

public class OperadoresAritmeticos2 {
    public static void main(String[] args) {
        int notaAluno1 = 99;
        int notaAluno2 = 80;
        int notaAluno3 = 53;

        int somaGeral = notaAluno1 + notaAluno2 + notaAluno3;

        System.out.println("Soma geral: " + somaGeral);

        int mediaGeral = (notaAluno1 + notaAluno2 + notaAluno3) / 3;

        System.out.println("Media geral: " + mediaGeral);
    }
}
